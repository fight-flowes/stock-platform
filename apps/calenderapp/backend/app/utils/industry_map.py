def get_full_industry_name(short_name: str) -> str:
    """
    获取完整的行业名称
    
    Args:
        short_name: Tushare 返回的简写名称
    
    Returns:
        完整的行业名称，如果映射不存在则返回原名
    """
    if not short_name:
        return short_name
    
    # 直接匹配
    if short_name in INDUSTRY_NAME_MAP:
        return INDUSTRY_NAME_MAP[short_name]
    
    # 去除罗马数字后缀再匹配
    import re
    clean_name = re.sub(r'[ⅠⅡⅢⅣⅤ]+$', '', short_name)
    if clean_name in INDUSTRY_NAME_MAP:
        return INDUSTRY_NAME_MAP[clean_name]
    
    return short_name